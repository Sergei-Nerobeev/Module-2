package com.javaacademy.homework3.task5;

public class Animal {
 private int lenghtBody;
 private int lenghtTail;

  public Animal(int lenghtBody, int lenghtTail) {
    this.lenghtBody = lenghtBody;
    this.lenghtTail = lenghtTail;
  }

  public int getLenghtBody() {
    return lenghtBody;
  }

  public int getLenghtTail() {
    return lenghtTail;
  }
}
